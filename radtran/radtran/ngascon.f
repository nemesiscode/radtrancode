	SUBROUTINE NGASCON(V0,ID,ISO,AMOUNT,PPRESS,
     &  PRESS,TEMP,ABSORB)
C     $Id: ngascon.f,v 1.4 2010-10-29 10:00:40 irwin Exp $
C-----------------------------------------------------------------------
C_TITLE:  NGASCON: to compute gaseous continuum spectra
C
C     Input variables
C         V0:REAL         wavenumber
C         ID:INTEGER      local gas identifier
C         ISO:INTEGER     local isotope identifier
C         AMOUNT:REAL     absorber amount
C         PPRESS:REAL     partial pressure of gas in atm
C         PRESS:REAL      total pressure in atm
C         TEMP:REAL       temp in K
C     Input variables from common block
C         IH2O:INTEGER    flag to turn H2O continuum on/off
C         ICH4:INTEGER    flag to turn CH4 continuum on/off
C     Output variable
C	  ABSORB:REAL	  Gas continuum absorption at V0 
C
C     Additional variables (common blocks)
C         DV1:REAL        width of H2O continuum bins       (10 /cm)
C         H2O***:REAL     H2O continuum data
C         NPT1:INTEGER    nr. of bins in each H2O*** array  (2001)
C         VB1:REAL        lower limit of H2O continuum data (0 /cm)
C         VT1:REAL        upper limit of H2O continuum data (20000 /cm)

C_KEYS:   SUBR,ATMO,SPEC,VMS
C
C_DESCR:  computes a continuum absorbtion to any known continuum spectra
C         for a particular gas over a defined wavenumber region.
C
C_REFS:	  CLOUGH S.A.,KNEIZYS F.X.,DAVIES R.,GAMACHE R.,TIPPING R. (1980
C         Theoretical line shape for H2O vapour: Application to the cont
C         Atmospheric Water Vapour, Eds. A.Deepak,T.D.Wilkeson,L.H.Ruhnk
C	  Academic Press, New York
C
C_FILES:  none
C
C_DATA:   /CH2OF0/	incorporated from H2OFT0.F
C	  /CH2OS0/	incorporated from H2OST0.F
C	  /CH2OS1/	incorporated from H2OST1.F
C	  /COMCON/	incorporated from GENDAT.F
C
C_BUGS:
C
C_HIST:   26nov86 SBC ORIGINAL VERSION
C          3feb88 SBC modified to return only one polynomial rather than
C                 for all layers in GENLBL
C	  27mar96 ILH modified to return a H20 continuum (based on GENLN2 
C		  sub-routine CONTUM.F)
C	  16jul01 PGJI modified to return single value.
C          3feb12 PGJI simplified and rationalised. Also added flag to 
C                      switch different absorptions on or off.
C-----------------------------------------------------------------------
C
	INTEGER ID,ISO,I,ICH4,IH2O
	REAL V0,DV,AMOUNT,PPRESS,PRESS,TEMP,ABSORB
        REAL CH4CONT
C
C --- Define COMMON blocks containing H2O continuum data ---
C
	COMMON /CH2OF0/ H2OF96(2001)
	COMMON /CH2OS0/ VB1,VT1,DV1,NPT1,H2OS96(2001)
	COMMON /CH2OS1/ H2OS60(2001)
C
C --- Define COMMON blocks containing physical constants ---
C
	COMMON /COMCON/ TS,PS,C2,PI
C
C --- Define COMMON block for Continuum switches

        COMMON /GASCONDEF/ ICH4,IH2O


C***********************************************************************
C ckd 2.1 self continuum data for water

       DIMENSION XFAC(0:50)
C-----------------------------------------------------------------------

       DATA (XFAC(I),I=0,50)/
     1    1.00000,1.01792,1.03767,1.05749,1.07730,1.09708,
     2    1.10489,1.11268,1.12047,1.12822,1.13597,1.14367,
     3    1.15135,1.15904,1.16669,1.17431,1.18786,1.20134,
     4    1.21479,1.22821,1.24158,1.26580,1.28991,1.28295,
     5    1.27600,1.26896,1.25550,1.24213,1.22879,1.21560,
     6    1.20230,1.18162,1.16112,1.14063,1.12016,1.10195,
     7    1.09207,1.08622,1.08105,1.07765,1.07398,1.06620,
     8    1.05791,1.04905,1.03976,1.02981,1.00985,1.00000,
     9    1.00000,1.00000,1.00000/
C-----------------------------------------------------------------------

C  scaling factors

       ALPHA2 = 200.0**2
       ALPHS2 = 120.0**2
       BETAS  = 5.0E-6
       V0S    = 1310.0
       FACTRS = 0.15

c ckd 2.1 foreign continuum data

       HWSQF  = 330.**2
       BETAF  = 8.  E-11
       V0F    = 1130.
       FACTRF = 0.97
       V0F2   = 1900.
       HWSQF2 = 150.**2
       BETA2  = 3.E-06


       C11 = 0.5 * C2 / TEMP
C
C --- Start calculations ---
C

       FF = V0
C
       IF (ID .EQ. 1. AND. IH2O. NE. 0) THEN 
C
C
	    IF (FF .GE. VB1 .AND. FF .LT. VT1) THEN
              MK = 1 + INT((FF-VB1)/DV1)
              IF(MK.EQ.NPT1) MK=NPT1-1
              F1 = VB1 + DV1*(MK-1)
              GR = (FF-F1)/DV1
C
C -- H2O Continuum --
C  

              CSFF60 = H2OS60(MK) + GR*(H2OS60(MK+1)-H2OS60(MK))       
              CSFF96 = H2OS96(MK) + GR*(H2OS96(MK+1)-H2OS96(MK))
              CFFF96 = H2OF96(MK) + GR*(H2OF96(MK+1)-H2OF96(MK))
C
C  Clough et el. use exponential extrapolation between temperatures
C
              TFAC =  (TEMP - 296.0)/(260.0 - 296.0)
              CSFFT = CSFF96*(CSFF60/CSFF96)**TFAC

c  calculate self modification factors between 700 and 1200 cm-1

              IF (FF .GE. 700.0  .AND. FF .LT. 1200.0) THEN
               IC = INT(0.1*(FF - 700.0))
               FC = 700.0 + IC*10.0
               GC = (FF - FC)/10.0
               SFAC = XFAC(IC) + GC*(XFAC(IC+1)-XFAC(IC))
              ELSE
               SFAC = 1.0
              END IF

              VS2    = (FF - V0S)**2



c  CKD 2.1 correction as of 94.4.28

              S1 = (1.0 - 0.2333*(ALPHA2/((FF-1050.0)**2 +
     &             ALPHA2)))
              S2 = (1.0 - FACTRS*(ALPHS2/(VS2 +
     &             (BETAS*VS2**2) + ALPHS2)))
              CSFFT = SFAC*S1*S2*CSFFT

c  foreign

              VF2 = (FF - V0F)**2
              VF6 = VF2 * VF2 * VF2
              FSCAL  = (1.- FACTRF* (HWSQF/(VF2+(BETAF*VF6)+
     &                 HWSQF)))
              VF2 = (FF - V0F2)**2
              VF4 = VF2*VF2
              FSCAL = FSCAL* (1.- 0.6*(HWSQF2/(VF2 + BETA2*VF4 +
     &                HWSQF2)))

c  CKD 2.1 correction as of 94.4.28

              CFFF96 = CFFF96 *FSCAL


              A1 = FF * AMOUNT * TANH(C11*FF)
              A2 = TS / (TEMP*PS)
              A3 = 1.0E-20 * (PPRESS*CSFFT + (PRESS-PPRESS)*CFFF96)
              ABSORB = A1 * A2 * A3

            ELSE

	      ABSORB=0.

	    ENDIF
C
C -- Other Gases --
C
       ELSEIF ((ID.EQ.6) .AND. ((ISO.EQ.1).OR.(ISO.EQ.0))
     &  .AND. ICH4.NE.0)THEN
C           Karkoschka CH4 coefficients

            ABSORB= AMOUNT*1E-20*CH4CONT(FF)

       ELSE

	    ABSORB=0.

       ENDIF
C

       RETURN
       END

