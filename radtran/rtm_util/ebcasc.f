     1092,092,022,092,092,092,092,004,092,092,092,092,020,021,092,026,
     1032,092,092,092,092,092,092,092,092,092,091,046,060,040,043,033,
     1038,092,092,092,092,092,092,092,092,092,093,036,042,041,059,094,
     1045,047,092,092,092,092,092,092,092,092,124,044,037,095,062,063,
     1092,092,092,092,092,092,092,092,092,096,058,035,064,039,061,034,
     1092,097,098,099,100,101,102,103,104,105,092,092,092,092,092,092,
     1092,106,107,108,109,110,111,112,113,114,092,092,092,092,092,092,
     1092,126,115,116,117,118,119,120,121,122,092,092,092,092,092,092,
     1092,092,092,092,092,092,092,092,092,092,092,092,092,092,092,092,
     1123,065,066,067,068,069,070,071,072,073,092,092,092,092,092,092,
     1125,074,075,076,077,078,079,080,081,082,092,092,092,092,092,092,
     1092,092,083,084,085,086,087,088,089,090,092,092,092,092,092,092,
     1048,049,050,051,052,053,054,055,056,057,092,092,092,092,092,255/
      DO 100 I=1,LEN(Q)
      IEBC=0
      CHEBC(BY)=Q(I:I)
      IASC=ASCII(IEBC)
      Q(I:I)=CHASC(BY)
100   CONTINUE
      RETURN
      END
