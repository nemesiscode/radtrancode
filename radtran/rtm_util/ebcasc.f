C     3/10/94   Updated Header          PGJI
C
C     ********************************************************************
      INTEGER BY
      PARAMETER(BY=1)
      CHARACTER Q*(*),CHEBC(4),CHASC(4)
      INTEGER ASCII(0:255),IEBC,IASC,I
      EQUIVALENCE(IEBC,CHEBC)
      EQUIVALENCE(IASC,CHASC)
      DATA ASCII/
     1000,001,002,003,092,009,092,127,092,092,092,011,012,013,014,015,
     1016,017,018,019,092,092,008,092,024,025,092,092,028,029,030,031,
