      SUBROUTINE EBCASC(Q)
C     $Id: ebcasc.f,v 1.1.1.1 2000-08-17 09:26:54 irwin Exp $
C     ********************************************************************
C     Translates the character string Q from EBCDIC to ASCII
C     Assumes a 4 byte integer in which byte BY corresponds to the character
C
